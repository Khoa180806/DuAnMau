    public boolean validateForm(boolean isCreate){
        String username = txtUsername.getText().trim();
        String fullname = txtFullname.getText().trim();
        String password = txtPassword.getText().trim();
        String confirmpass = txtConfirmPassword.getText().trim();
        
        // Kiểm tra rỗng
        if (username.equals("") || fullname.equals("") || password.equals("") || confirmpass.equals("")) {
            XDialog.alert("Vui lòng nhập đầy đủ thông tin");
            return false;
        }

        // Kiểm tra độ dài username
        if (username.length() < 5 || username.length() > 50) {
            XDialog.alert("Tên đăng nhập phải từ 5-50 ký tự!");
            return false;
        }

        // Kiểm tra độ phức tạp password
        if (!password.matches("^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,}$")) {
            XDialog.alert("Mật khẩu phải có ít nhất 8 ký tự, bao gồm chữ và số!");
            return false;
        }

        // Kiểm tra mật khẩu khớp
        if (!password.equals(confirmpass)) {
            XDialog.alert("Mật khẩu không khớp!");
            return false;
        }

        // Kiểm tra fullname hợp lệ
        if (!fullname.matches("^[\\p{L}\\s]+$")) {
            XDialog.alert("Họ tên không được chứa ký tự đặc biệt!");
            return false;
        }
        
        // Kiểm tra username đã tồn tại khi tạo mới
        if (isCreate) {
            User existingUser = dao.findById(username);
            if (existingUser != null) {
                XDialog.alert("Tên đăng nhập đã tồn tại!");
                return false;
            }
        }
        return true;
    }
    
    @Override 
    public void open() { 
        this.setLocationRelativeTo(null); 
        this.fillToTable(); 
        this.clear(); 
    } 

    @Override 
    public void fillToTable() { 
        DefaultTableModel model = (DefaultTableModel) tblUsers.getModel(); 
        model.setRowCount(0); 

        items = dao.findAll(); 
        items.forEach(item -> { 
            Object[] rowData = { 
                item.getUsername(), 
                item.getPassword(), 
                item.getFullname(),
                item.getPhoto(),
                item.isManager() ? "Quản lý" : "Nhân viên",
                item.isEnabled() ? "Hoạt động" : "Tạm dừng",
                false 
            }; 
            model.addRow(rowData); 
        }); 
    } 
    
    @Override 
    public void edit() { 
        User entity = items.get(tblUsers.getSelectedRow()); 
        this.setForm(entity); 
        this.setEditable(true); 
        tabs.setSelectedIndex(1); 
    } 

