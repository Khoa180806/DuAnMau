    @Override 
    public void moveTo(int index) { 
        if (index < 0) { 
            this.moveLast(); 
        } else if (index >= tblUsers.getRowCount()) { 
            this.moveFirst(); 
        } else { 
            tblUsers.clearSelection(); 
            tblUsers.setRowSelectionInterval(index, index); 
            this.edit(); 
        } 
    } 
    
