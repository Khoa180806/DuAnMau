    @Override 
    public void create() { 
        if (validateForm(true)) {
            User entity = this.getForm();
            try {
                dao.create(entity);
                this.fillToTable();
                this.clear();
                XDialog.alert("Thêm mới thành công!");
            } catch (Exception e) {
                XDialog.alert("Thêm mới thất bại!");
            }
        }
    } 

    @Override 
    public void update() { 
        if (validateForm(false)) {
            User entity = this.getForm();
            try {
                // Kiểm tra user tồn tại
                if (dao.findById(entity.getUsername()) == null) {
                    XDialog.alert("Người dùng không tồn tại!");
                    return;
                }
                dao.update(entity);
                this.fillToTable();
                XDialog.alert("Cập nhật thành công!");
            } catch (Exception e) {
                XDialog.alert("Cập nhật thất bại!");
            }
        }
    } 

    @Override 
    public void delete() { 
        if (XDialog.confirm("Bạn thực sự muốn xóa?")) {
            String username = txtUsername.getText();
            try {
                // Kiểm tra ràng buộc trước khi xóa
                if (hasReferences(username)) {
                    XDialog.alert("Không thể xóa người dùng này do đang được sử dụng!");
                    return;
                }
                dao.deleteById(username);
                this.fillToTable();
                this.clear();
                XDialog.alert("Xóa thành công!");
            } catch (Exception e) {
                XDialog.alert("Xóa thất bại!");
            }
        }
    } 

